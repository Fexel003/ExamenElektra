        private async void btnAccion_Click(object sender, RoutedEventArgs e)
        {
            HttpClient client = new HttpClient();
            client.BaseAddress = new Uri("https://api.github.com/users");
            client.DefaultRequestHeaders.Accept.Clear();
            client.DefaultRequestHeaders.Accept.Add(new MediaTypeWithQualityHeaderValue("application/json"));
            client.DefaultRequestHeaders.Add("User-Agent", "Awesome-Octocat-App");

            HttpResponseMessage response = await client.GetAsync("https://api.github.com/users");
            if (response.IsSuccessStatusCode)
            {
                var json = await response.Content.ReadAsStringAsync();
                var repos = System.Text.Json.JsonSerializer.Deserialize<List<users>>(json);
                txtTextoApi.Text =  "resultado = login: " + repos[0].login + ", id: " + repos[0].id + " type: " + repos[0].type;
            }
        }
    }

